def afficher_positif_negatif(nombre):
    if nombre > 0:
        print("positif")
    elif nombre < 0:
        print("negatif")
    else:
        print("Le nombre est égal à zéro")

afficher_positif_negatif(-18)
afficher_positif_negatif(5)
afficher_positif_negatif(-3)
afficher_positif_negatif(8)
